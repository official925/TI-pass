let index = 0;
let showingAnswer = false;
let autoMode = false;
let interval = null;

function renderCard() {
  document.getElementById("question").textContent = cards[index].q;
  document.getElementById("answer").textContent = cards[index].a;
  document.getElementById("answer").classList.add("hidden");
  showingAnswer = false;

  // 進捗表示
  document.getElementById("counter").textContent =
    `${index + 1} / ${cards.length}（${Math.round((index + 1) / cards.length * 100)}%）`;
}

function flipCard() {
  const ans = document.getElementById("answer");
  showingAnswer = !showingAnswer;
  ans.classList.toggle("hidden", !showingAnswer);
}

function nextCard() {
  index = (index + 1) % cards.length;
  renderCard();
}

function prevCard() {
  index = (index - 1 + cards.length) % cards.length;
  renderCard();
}

function toggleAuto() {
  autoMode = !autoMode;
  const btn = document.getElementById("autoBtn");
  const sec = Number(document.getElementById("intervalInput").value) || 3;

  if (autoMode) {
    btn.textContent = "停止";
    interval = setInterval(() => {
      if (!showingAnswer) {
        flipCard();
      } else {
        nextCard();
      }
    }, sec * 1000);
  } else {
    btn.textContent = "自動";
    clearInterval(interval);
  }
}

